const languageSelector = document.querySelector("select")

languageSelector.addEventListener("change", (event) => {
    setLanguage(event.target.value)
    localStorage.setItem("language", event.target.value)
    if (event.target.value === 'ar') {
        col2.style.textAlign = "right";
        ProductsAndServices.style.textAlign = "right";
        ProductsAndServicesDesc.style.textAlign = "right";
        QualityAndCommitment.style.textAlign = "right";
        QualityAndCommitmentDesc.style.textAlign = "right";
        footerDesc.style.textAlign = "right";
    } else {
        col2.style.textAlign = "left";
        ProductsAndServices.style.textAlign = "left";
        ProductsAndServicesDesc.style.textAlign = "left";
        QualityAndCommitment.style.textAlign = "left";
        QualityAndCommitmentDesc.style.textAlign = "left";
        footerDesc.style.textAlign = "left";
    }
});

document.addEventListener("DOMContentLoaded", () => {
    setLanguage(localStorage.getItem("language"));
});

const setLanguage = (language) => {
    const elements = document.querySelectorAll("[data-i18n]")
    elements.forEach((element) => {
        const translationKey = element.getAttribute('data-i18n')
        element.textContent = translations[language][translationKey]
    });
    document.dir = language === 'ar' ? 'rtl' : 'ltr';
};

